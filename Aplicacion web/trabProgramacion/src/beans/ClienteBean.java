package beans;

public class ClienteBean {
	
	private int idperson;
	private String name;
	private String last_name1;
	private String last_name2;
	private String user;
	private String pwd;
	private char gender;
	private String date_birth;
	public int getIdperson() {
		return idperson;
	}
	public void setIdperson(int idperson) {
		this.idperson = idperson;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getLast_name1() {
		return last_name1;
	}
	public void setLast_name1(String last_name1) {
		this.last_name1 = last_name1;
	}
	public String getLast_name2() {
		return last_name2;
	}
	public void setLast_name2(String last_name2) {
		this.last_name2 = last_name2;
	}
	public String getUser() {
		return user;
	}
	public void setUser(String user) {
		this.user = user;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	public char getGender() {
		return gender;
	}
	public void setGender(char gender) {
		this.gender = gender;
	}
	public String getDate_birth() {
		return date_birth;
	}
	public void setDate_birth(String date_birth) {
		this.date_birth = date_birth;
	}
	

}
